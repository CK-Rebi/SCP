import turtle as t
t.speed(0)

#画脸
t.penup()
t.setheading(270)
t.forward(150)#半径
t.left(90)
t.pendown()
t.pensize(10)
t.color('orange','yellow')
t.begin_fill()
t.circle(150)#逆时针360画圆
t.end_fill()#大黄脸填充好颜色

#画嘴角
t.pencolor('brown')
t.penup()
t.left(90)
t.penup()
t.forward(30)
t.right(90)
t.pendown()
t.circle(120,90)
t.circle(120,-180)

#画左脸蛋的红晕
t.left(180)
t.penup()
t.fd(10)
t.right(90)
t.fd(30)
t.pensize(2)
t.color('yellow','red')
t.begin_fill()
t.circle(15)
t.end_fill()

#画右脸蛋的红晕
t.penup()
t.fd(180)
t.pensize(2)
t.color('yellow','red')
t.begin_fill()
t.circle(15)
t.end_fill()

#画右眼睛
t.penup()
t.goto(0,0)
t.left(90)
t.fd(40)
t.right(90)
t.fd(50)
t.pendown()
t.setheading(40)
t.pensize(10)
t.color('orange')
t.circle(-100,15)
t.pensize(3)
t.color('black')
t.begin_fill()
t.circle(8)
t.end_fill()
t.pensize(10)
t.color('orange')
t.circle(-100,55)
t.left(125)
t.fd(25)
t.left(55)
t.circle(100,70)
t.left(50)
t.fd(10)
t.pensize(10)
t.color('orange')
t.fd(15)

#画左眼睛
t.penup()
t.right(90)
t.fd(100)
t.pendown()
t.setheading(140)
t.pensize(10)
t.color('orange')
t.circle(100,55)
t.pensize(3)
t.color('black')
t.begin_fill()
t.circle(-9)
t.end_fill()
t.pensize(10)
t.color('orange')
t.circle(100,15)
t.right(120)
t.fd(25)
t.right(60)
t.circle(-100,70)
t.right(55)
t.fd(25)
t.pensize(10)

#画左边大黑眉毛
t.penup()
t.left(180)
t.fd(50)
t.pendown()
t.pensize(10)
t.color('black')
t.setheading(110)
t.circle(90,75)
t.right(180)
t.circle(-90,80)

#画右边大黑眉毛
t.left(80)
t.penup()
t.fd(100)
t.pendown()
t.pensize(10)
t.color('black')
t.setheading(70)
t.circle(-90,75)

